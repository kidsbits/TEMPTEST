class OLED:
    def __init__(self, i2c, width=128, height=64, addr=0x3C):
        self.i2c = i2c
        self.width = width
        self.height = height
        self.addr = addr
        self.oled = ssh1106.SSH1106(self.i2c,self.width, self.height,  self.addr)

    def clear(self):
        self.oled.fill(0)
        self.oled.show()

    def show_text(self, text, x=0, y=0):
        self.oled.text(text, x+1, y)
        self.oled.show()

    def show_image(self, image, x=0, y=0):
        for i in range(image.height):
            for j in range(image.width):
                self.oled.pixel(x+j, y+i, image.getpixel((j, i)))
        self.oled.show()
    # 显示箭头的函数
    def show_arrow(self,arrow, width, height):
        for y in range(height):
            for x in range(width):
                byte_index = (x // 8) + (y * (width // 8))
                bit_index = x % 8
                if byte_index < len(arrow):
                    if (arrow[byte_index] >> bit_index) & 1:
                        self.oled.pixel(x, y, 1)
